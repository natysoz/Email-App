import storageService from './storage-services';

var gMails=[];

function query() {
    return storageService.loadFromStorage('mails')
        .then(mails => {
            if (!mails || !mails.length) {
                mails = createFakeMails();
                gMails = mails;
                storageService.saveToStorage('mails', mails);
            }
            gMails = mails;
            return mails;
        })
}

function getMailStatus(){
    return {
        unread: this.getUnreadMails(),
        deleted: this.getDeletedMails(),
    }
}


function getUnreadMails() {
    return gMails.filter(mail=>mail.isRead)
}

function getDeletedMails() {
    return gMails.filter(mail=>mail.isDeleted)
}

function updateMailStatus(mailID){
    return storageService.loadFromStorage('mails')
        .then(mails => {
            var emailIdx = mails.findIndex(email => email._id === mailID);
            gMails[emailIdx].isRead =true;
            return storageService.saveToStorage('mails', gMails);
        });
}

function deleteMailByID(mailID) {
    return storageService.loadFromStorage('mails')
        .then(mails => {
            var emailIdx = mails.findIndex(email => email._id === mailID);
            gMails.splice(emailIdx, 1);
            //console.log('to delete ',mailID,'Found idx of this ID',emailIdx);
            return storageService.saveToStorage('mails', gMails);
        });
}


function pushNewMail(mail){
    let newMail = createNewMail(mail);
    gMails.unshift(newMail);
    storageService.saveToStorage('mails', gMails);
}


