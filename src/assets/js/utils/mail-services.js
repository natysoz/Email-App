function getMailByID(mailID) {
    return storageService.load('mails')
        .then(emails => {
            return emails.find(email => email.id === mailID);
        })
}

function deleteMailByID(mailID) {
    return storageService.load('mails')
        .then(mails => {
            var mailIdx = mails.findIndex(mail => mail.id === mailID);
            mails.splice(mailIdx, 1);
            return storageService.store('mails', mails);
        })
}

